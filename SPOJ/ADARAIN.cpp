#define gc(x) getchar_unlocked(x)
#define pc(x) putchar_unlocked(x)
#define endline pc('\n')

typedef long long LL;

struct FastIO {
	template <typename T> T
	getnumb() {
		T c = gc();
		
		std::function<bool()> is_negative = [&]() -> bool {
			for (; c < '0' || c > '9'; c = gc()) {
				if (c == '-') {
					c = gc();
					if (c >= '0' && c <= '9')
						return true;
				}
			}
			return false;
		};
		
		bool negative = is_negative();
		T val = 0;
		
		for (; c >= '0' && c <= '9'; c = gc())
			val = (val << 1) + (val << 3) + c - (T)'0';
		
		return negative ? -val : val;
	}
	
	std::string
	getstr(bool line_scan = false) {
		std::function<bool(char&)> is_separator = [&](char &c) -> bool {
			return c == '\n' || (c == ' ' && !line_scan) || c == EOF;
		};
		
		char c;
		std::string str = "";
		
		while ((c = gc()) && is_separator(c));
		do {
			str += c;
		} while ((c = gc()) && !is_separator(c));
		
		return str;
	}
	
	template <typename T> void
	printnumb(T val, std::string suffix = "") {
		std::function<void(T)> _print = [&](T x) -> void {
			if (x == 0)
				return;
			
			_print(x / 10);
			pc('0' + (x % 10));
		};
		
		if (val != 0) {
			if (val < 0) {
				pc('-');
				val = -val;
			}
			_print(val);
		} else {
			pc('0');
		}
		printstr(suffix);
	}
	
	void
	printstr(std::string &str) {
		for (char &c: str)
			pc(c);
	}
} fio;


const int MAX_N = 1e6;

int n;
int ans[MAX_N + 1];

void
set_ans(int& rain) {
	for (int i = 0; i < rain; i ++) {
		int a = fio.getnumb<int>();
		int b = fio.getnumb<int>();
		
		ans[a]++;
		ans[b + 1]--;
	}
	
	int curr = 0;
	
	for (int i = 0; i < n; i++) {
		curr += ans[i];
		ans[i] = curr;
	}
}

int
main() {
	int rain = fio.getnumb<int>();
	int que = fio.getnumb<int>();
	
	n = fio.getnumb<int>();
	set_ans(rain);
	
	while (que--) {
		int x = fio.getnumb<int>();
		fio.printnumb<int>(ans[x], "\n");
	}
	
	return 0;
}

